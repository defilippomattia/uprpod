-- Dostupni sadrzaji za sportski objekt

SELECT SPORTSKI_OBJEKT.IME, DODATNI_SADRZAJ.TIP
FROM SPORTSKI_OBJEKT
INNER JOIN OBJEKT_SADRZAJ
ON OBJEKT_SADRZAJ.SPORTSKI_OBJEKT_ID = SPORTSKI_OBJEKT.SPORTSKI_OBJEKT_ID
INNER JOIN DODATNI_SADRZAJ
ON DODATNI_SADRZAJ.DODATNI_SADRZAJ_ID = objekt_sadrzaj.dodatni_sadrzaj_id


-- Dostupni rekviziti

