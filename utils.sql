-- Korisnici moraju vidjeti sve dostupne terene u njihovoj blizini (po kvartovima) te vidjeti postoji li slobodan termin.

-- tereni u kvartu
SELECT KVART.IME, TEREN.IME 
FROM TEREN
INNER JOIN KVART
ON TEREN.KVART_ID = KVART.KVART_ID

-- tereni u kvartu sa slobodnim terminom (treba jos malo sredit, vjv LEFT JOIN...)
SELECT REZERVACIJA.POCETAK, KVART.IME, TEREN.IME 
FROM TEREN
INNER JOIN KVART
ON TEREN.KVART_ID = KVART.KVART_ID
INNER JOIN REZERVACIJA
ON TEREN.KVART_ID = REZERVACIJA.TEREN_ID

